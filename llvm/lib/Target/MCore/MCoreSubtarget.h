#ifndef LLVM_LIB_TARGET_MCORE_MCORESUBTARGET_H
#define LLVM_LIB_TARGET_MCORE_MCORESUBTARGET_H

#include "MCoreFrameLowering.h"
#include "MCoreISelLowering.h"
#include "MCoreInstrInfo.h"
#include "MCoreSelectionDAGInfo.h"
#include "llvm/CodeGen/TargetSubtargetInfo.h"
#include "llvm/IR/DataLayout.h"
#include "llvm/Target/TargetMachine.h"
#include <string>

#define GET_SUBTARGETINFO_HEADER
#include "MCoreGenSubtargetInfo.inc"

namespace llvm {
class StringRef;

class MCoreSubtarget : public MCoreGenSubtargetInfo {
  virtual void anchor();
  MCoreFrameLowering FrameLowering;
  MCoreInstrInfo InstrInfo;
  MCoreRegisterInfo RegInfo;
  MCoreTargetLowering TLInfo;
  MCoreSelectionDAGInfo TSInfo;

public:
  /// This constructor initializes the data members to match that
  /// of the specified triple.
  ///
  MCoreSubtarget(const Triple &TT, const std::string &CPU,
                 const std::string &FS, const TargetMachine &TM);

  /// ParseSubtargetFeatures - Parses features string setting specified
  /// subtarget options.  Definition of function is auto generated by tblgen.
  void ParseSubtargetFeatures(StringRef CPU, StringRef FS);

  const MCoreInstrInfo *getInstrInfo() const override {
    return &InstrInfo;
  }
  const MCoreFrameLowering *getFrameLowering() const override {
    return &FrameLowering;
  }
  const MCoreTargetLowering *getTargetLowering() const override {
    return &TLInfo;
  }
  const MCoreSelectionDAGInfo *getSelectionDAGInfo() const override {
    return &TSInfo;
  }
  const MCoreRegisterInfo *getRegisterInfo() const override {
    return &RegInfo;
  }
};
} // End llvm namespace

#endif
